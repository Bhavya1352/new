let currentDir = 'ltr';

function updateHeroHeadingPunctuation() {
    const heroHeadings = document.querySelectorAll('.hero .hero-content h1');

    heroHeadings.forEach((heading) => {
        if (!heading.dataset.baseHeading) {
            heading.dataset.baseHeading = heading.textContent.trim();
        }

        const baseHeading = heading.dataset.baseHeading;
        const punctuationMatch = baseHeading.match(/[.!?:]$/);

        if (currentDir === 'rtl') {
            if (punctuationMatch) {
                const punctuation = punctuationMatch[0];
                const textWithoutPunctuation = baseHeading.slice(0, -1).trim();
                heading.textContent = `${punctuation} ${textWithoutPunctuation}`;
            } else {
                heading.textContent = `: ${baseHeading}`;
            }
        } else {
            heading.textContent = baseHeading;
        }
    });
}

function toggleDirection() {
    currentDir = currentDir === 'ltr' ? 'rtl' : 'ltr';
    document.documentElement.setAttribute('dir', currentDir);
    updateHeroHeadingPunctuation();
}

document.addEventListener('DOMContentLoaded', () => {
    updateHeroHeadingPunctuation();

    const langToggle = document.getElementById('langToggle');
    if (langToggle) {
        langToggle.addEventListener('click', toggleDirection);
    }

    const menuToggle = document.getElementById('menuToggle');
    const navMenu = document.querySelector('.nav-menu');
    
    if (menuToggle && navMenu) {
        menuToggle.addEventListener('click', () => {
            navMenu.classList.toggle('active');
        });
    }

    const forms = document.querySelectorAll('form');
    forms.forEach(form => {
        form.addEventListener('submit', (e) => {
            e.preventDefault();
            alert('Form submitted successfully!');
        });
    });
});
